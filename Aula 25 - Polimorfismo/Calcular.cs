using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Aula_25___Polimorfismo
{
    public class Calcular
    {
        public int Somar(int n1, int n2)
        {
            return n1 + n2;
        }

        public int Somar(int n1, int n2, int n3)
        {
            return n1 + n2 + n3;
        }

    }
}
